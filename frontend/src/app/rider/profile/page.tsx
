
'use client'

import { useState, useEffect } from 'react'
import { 
  User, 
  Mail, 
  Phone, 
  Truck, 
  Save,
  CheckCircle,
  Star,
  Package
} from 'lucide-react'

interface RiderProfile {
  _id: string
  name: string
  email: string
  phone: string
  photo?: string
  vehicle: string
  rating: number
  totalDeliveries: number
  status: string
}

export default function RiderProfilePage() {
  const [profile, setProfile] = useState<RiderProfile | null>(null)
  const [loading, setLoading] = useState(true)
  const [saving, setSaving] = useState(false)
  const [saved, setSaved] = useState(false)
  
  const [name, setName] = useState('')
  const [phone, setPhone] = useState('')

  useEffect(() => {
    fetchProfile()
  }, [])

  const fetchProfile = async () => {
    try {
      const riderToken = localStorage.getItem('riderToken')
      const res = await fetch('http://localhost:5000/api/riders/profile', {
        headers: { 'Authorization': `Bearer ${riderToken}` }
      })
      const data = await res.json()
      setProfile(data)
      setName(data.name || '')
      setPhone(data.phone || '')
    } catch (error) {
      console.error('Error fetching profile:', error)
    } finally {
      setLoading(false)
    }
  }

  const handleSaveProfile = async () => {
    setSaving(true)
    try {
      const riderToken = localStorage.getItem('riderToken')
      const res = await fetch('http://localhost:5000/api/riders/profile', {
        method: 'PUT',
        headers: {
          'Authorization': `Bearer ${riderToken}`,
          'Content-Type': 'application/json'
        },
        body: JSON.stringify({ name, phone })
      })

      if (res.ok) {
        setSaved(true)
        setTimeout(() => setSaved(false), 3000)
        fetchProfile()
      }
    } catch (error) {
      console.error('Error saving profile:', error)
    } finally {
      setSaving(false)
    }
  }

  if (loading) {
    return (
      <div className="p-6 flex items-center justify-center min-h-[60vh]">
        <div className="animate-spin rounded-full h-12 w-12 border-b-2 border-orange-500"></div>
      </div>
    )
  }

  return (
    <div className="p-6">
      {/* Header */}
      <div className="mb-6">
        <h1 className="text-2xl font-bold text-white">Profile</h1>
        <p className="text-slate-400">Manage your rider account</p>
      </div>

      <div className="grid grid-cols-1 lg:grid-cols-2 gap-6">
        {/* Profile Info */}
        <div className="bg-slate-800 rounded-xl p-6">
          <h3 className="text-lg font-semibold text-white flex items-center gap-2 mb-6">
            <User className="w-5 h-5" /> Profile Information
          </h3>
          
          <div className="space-y-4">
            <div className="flex items-center gap-4 p-4 bg-slate-700/50 rounded-lg">
              <div className="w-16 h-16 bg-gradient-to-br from-orange-500 to-red-600 rounded-full flex items-center justify-center">
                {profile?.photo ? (
                  <img src={profile.photo} alt={profile.name} className="w-full h-full rounded-full object-cover" />
                ) : (
                  <Truck className="w-8 h-8 text-white" />
                )}
              </div>
              <div>
                <p className="text-white font-medium">{profile?.name}</p>
                <p className="text-slate-400 text-sm">{profile?.email}</p>
              </div>
            </div>

            <div>
              <label className="block text-sm font-medium text-slate-300 mb-1">Name</label>
              <input
                type="text"
                value={name}
                onChange={(e) => setName(e.target.value)}
                className="w-full px-3 py-2 bg-slate-700 border border-slate-600 rounded-lg text-white focus:outline-none focus:ring-2 focus:ring-orange-500"
              />
            </div>

            <div>
              <label className="block text-sm font-medium text-slate-300 mb-1">Phone</label>
              <input
                type="tel"
                value={phone}
                onChange={(e) => setPhone(e.target.value)}
                className="w-full px-3 py-2 bg-slate-700 border border-slate-600 rounded-lg text-white focus:outline-none focus:ring-2 focus:ring-orange-500"
              />
            </div>

            <div>
              <label className="block text-sm font-medium text-slate-300 mb-1">Email</label>
              <input
                type="email"
                value={profile?.email || ''}
                disabled
                className="w-full px-3 py-2 bg-slate-700/50 border border-slate-600 rounded-lg text-slate-400 cursor-not-allowed"
              />
            </div>

            <div>
              <label className="block text-sm font-medium text-slate-300 mb-1">Vehicle</label>
              <input
                type="text"
                value={profile?.vehicle || ''}
                disabled
                className="w-full px-3 py-2 bg-slate-700/50 border border-slate-600 rounded-lg text-slate-400 cursor-not-allowed"
              />
            </div>

            <button
              onClick={handleSaveProfile}
              disabled={saving}
              className="flex items-center gap-2 px-4 py-2 bg-orange-600 text-white rounded-lg hover:bg-orange-700 disabled:opacity-50 transition-colors"
            >
              {saved ? <CheckCircle className="w-5 h-5" /> : <Save className="w-5 h-5" />}
              {saving ? 'Saving...' : saved ? 'Saved!' : 'Save Profile'}
            </button>
          </div>
        </div>

        {/* Stats */}
        <div className="space-y-6">
          <div className="bg-slate-800 rounded-xl p-6">
            <h3 className="text-lg font-semibold text-white mb-6">Performance</h3>
            <div className="grid grid-cols-2 gap-4">
              <div className="bg-slate-700/50 p-4 rounded-lg text-center">
                <div className="flex items-center justify-center gap-1 mb-2">
                  <Star className="w-5 h-5 text-yellow-400" />
                  <span className="text-2xl font-bold text-white">{profile?.rating?.toFixed(1) || '0.0'}</span>
                </div>
                <p className="text-slate-400 text-sm">Rating</p>
              </div>
              <div className="bg-slate-700/50 p-4 rounded-lg text-center">
                <div className="flex items-center justify-center gap-1 mb-2">
                  <Package className="w-5 h-5 text-green-400" />
                  <span className="text-2xl font-bold text-white">{profile?.totalDeliveries || 0}</span>
                </div>
                <p className="text-slate-400 text-sm">Deliveries</p>
              </div>
            </div>
          </div>

          <div className="bg-slate-800 rounded-xl p-6">
            <h3 className="text-lg font-semibold text-white mb-4">Current Status</h3>
            <div className="flex items-center gap-3">
              <span className={`px-3 py-1 rounded-full text-sm font-medium ${
                profile?.status === 'active' ? 'bg-green-900 text-green-300' :
                profile?.status === 'busy' ? 'bg-orange-900 text-orange-300' :
                'bg-gray-600 text-gray-300'
              }`}>
                {profile?.status === 'active' ? 'Online' : 
                 profile?.status === 'busy' ? 'On Delivery' : 'Offline'}
              </span>
            </div>
          </div>
        </div>
      </div>
    </div>
  )
}


